const _ = require('lodash');
const config = require('../../config');

//----------------------------------------------------------------------------------------------------------------------

class ConfigManager
{
    constructor()
    {
        this._config = config;
    } // end constructor

    //------------------------------------------------------------------------------------------------------------------
    // Properties
    //------------------------------------------------------------------------------------------------------------------

    get config(){ return this._config; }

    //------------------------------------------------------------------------------------------------------------------
    // Public
    //------------------------------------------------------------------------------------------------------------------

    get(...args)
    {
        return _.get(config, ...args);
    } // end get
} // end ConfigManager

//----------------------------------------------------------------------------------------------------------------------

module.exports = new ConfigManager();

