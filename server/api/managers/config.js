//----------------------------------------------------------------------------------------------------------------------
// ConfigManager
//----------------------------------------------------------------------------------------------------------------------

const _ = require('lodash');

//----------------------------------------------------------------------------------------------------------------------

class ConfigManager
{
    constructor()
    {
        this._config = require('../../../config');
    } // end constructor

    //------------------------------------------------------------------------------------------------------------------
    // Properties
    //------------------------------------------------------------------------------------------------------------------

    get config(){ return this._config; }

    //------------------------------------------------------------------------------------------------------------------
    // Public
    //------------------------------------------------------------------------------------------------------------------

    get(...args)
    {
        return _.get(this._config, ...args);
    } // end get

    set(...args)
    {
        _.set(this._config, ...args);
    } // end set
} // end ConfigManager

//----------------------------------------------------------------------------------------------------------------------

module.exports = new ConfigManager();

