<template>
    <div id="page-display" v-html="renderedBody"></div>
</template>

<!--------------------------------------------------------------------------------------------------------------------->

<style lang="scss" scoped>
    #page-display {
    }
</style>

<!--------------------------------------------------------------------------------------------------------------------->

<script>
    //------------------------------------------------------------------------------------------------------------------

	import markdown from '../../../lib/markdown';

	// Managers
	import pageMan from '../../../api/managers/page';

    //------------------------------------------------------------------------------------------------------------------

    export default {
		computed: {
			renderedBody()
			{
				if(this.page)
				{
					return markdown.render(this.page.body);
				}
				else
				{
					return '';
				} // end if
			}
		},
		subscriptions: {
        	page: pageMan.currentPage$
		}
    }
</script>

