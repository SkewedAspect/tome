import $http from 'axios';

import BaseService from './base';

//----------------------------------------------------------------------------------------------------------------------

class WikiPageService extends BaseService {
    constructor()
    {
        super();
    } // end constructor

    pageExists(path)
    {
        return $http.head(`/wiki${ path }`)
            .then(() => true)
            .catch(() => false);
    } // end pageExists
} // end WikiPageService

//----------------------------------------------------------------------------------------------------------------------

export default new WikiPageService();

